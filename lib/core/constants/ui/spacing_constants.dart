/// Semantic spacing constants for gaps between elements.
///
/// Provides raw values for spacing.
/// For pre-computed SizedBox widgets, see `SpacingWidgets`.
abstract final class SpacingConstants {
  /// Hairline spacing: 1.0
  static const double hairline = 1;

  /// Extra small gap: 4.0
  static const double xs = 4;

  /// Small gap: 8.0
  static const double sm = 8;

  /// Medium gap: 16.0
  static const double md = 16;

  /// Large gap: 24.0
  static const double lg = 24;

  /// Extra large gap: 32.0
  static const double xl = 32;

  /// 2X large gap: 48.0
  static const double xxl = 48;
}
