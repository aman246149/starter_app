/// Opacity constants for disabled states, overlays, and emphasis levels.
///
/// These values follow Material Design 3 opacity specifications
/// for consistent visual hierarchy and state representation.
abstract final class OpacityConstants {
  /// Fully transparent: 0.0
  static const double transparent = 0;

  /// Disabled state: 0.38 (Material Design standard)
  static const double disabled = 0.38;

  /// Medium emphasis: 0.60
  static const double mediumEmphasis = 0.60;

  /// High emphasis: 0.87
  static const double highEmphasis = 0.87;

  /// Scrim/overlay background: 0.32
  static const double scrim = 0.32;

  /// Hover state: 0.08
  static const double hover = 0.08;

  /// Focus state: 0.12
  static const double focus = 0.12;

  /// Pressed state: 0.12
  static const double pressed = 0.12;

  /// Fully opaque: 1.0
  static const double opaque = 1;
}
