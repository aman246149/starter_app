/// Border radius constants for consistent corner rounding.
///
/// Provides raw values for border radius.
/// For pre-computed BorderRadius widgets, see `BorderRadiusWidgets`.
abstract final class BorderRadiusConstants {
  /// Small border radius: 8.0
  static const double small = 8;

  /// Medium border radius: 12.0
  static const double medium = 12;

  /// Large border radius: 16.0
  static const double large = 16;

  /// Extra large border radius: 24.0
  static const double xLarge = 24;

  /// Circular border radius (for circular elements)
  static const double circular = 999;
}
